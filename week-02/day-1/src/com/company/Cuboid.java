package com.company;

public class Cuboid {
    public static void main(String[] args) {
        double l = 10;
        double w = 10;
        double h = 10;

        double area = l * w;
        double volume = l * w * h;

        System.out.println("Surface area: " + area);
        System.out.println("Volume: " + volume);
    }
}
