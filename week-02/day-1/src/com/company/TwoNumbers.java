package com.company;

public class TwoNumbers {
    public static void main(String[] args) {
        int num1 = 22;
        int num2 = 13;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(22.0 / 13.0);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
    }
}
